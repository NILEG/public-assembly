INCLUDE IRVINE32.INC
.data
a DWORD ?
b DWORD ?
msg1 BYTE "Enter 1: ",0
msg2 BYTE "Enter 2: ",0
.code
MAIN PROC
CALL CLRSCR

comment &
LEA EDX, msg1
CALL WRITESTRING
CALL READINT
MOV a, EAX
CALL CRLF

LEA EDX, msg2
CALL WRITESTRING
CALL READINT
MOV b, EAX
CALL CRLF

MOV EBX, a
MOV ECX, b
MOV a, ECX
MOV b, EBX

MOV EAX, a
CALL WRITEDEC
CALL CRLF

MOV EAX, b
CALL WRITEDEC
CALL CRLF
&

LEA EDX, msg1
CALL WRITESTRING
CALL READINT
MOV EBX, EAX

LEA EDX, msg2
CALL WRITESTRING
CALL READINT
XCHG EBX, EAX

CALL WRITEDEC
CALL CRLF
MOV EAX, EBX
CALL WRITEDEC
EXIT
MAIN ENDP
END MAIN

